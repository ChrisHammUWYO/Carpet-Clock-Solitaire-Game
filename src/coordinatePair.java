public class coordinatePair
{
	private int x; //the x value
	private int y; //the y value
	public coordinatePair(int inputX, int inputY)
	{
		//default constructor
		x= inputX;
		y= inputY;
	}
	public void setX(int inputX)
	{
		x= inputX;
	}
	public void setY(int inputY)
	{
		y= inputY;
	}
	public int getX()
	{
		return x;
	}
	public int getY()
	{
		return y;
	}
	
}//end of class 
